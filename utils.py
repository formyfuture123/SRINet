def sampleHighAdj(adj):
    ones = tf.ones_like(adj.values)
    zeros = tf.zeros_like(adj.values)
    high_adj_values = tf.where(adj.values < 5., x=zeros,  y=ones)
    return tf.multiply(high_adj_values, adj.values)
def samplePred(pred, train_edges_true, train_edges_false, num_nodes):
    pred_friend = []
    for et in train_edges_true:
        pred_friend.append(pred[num_nodes*et[0] + et[1]])
    for ef in train_edges_false:
        pred_friend.append(pred[num_nodes*ef[0] + ef[1]])
    return tf.convert_to_tensor(pred_friend)


